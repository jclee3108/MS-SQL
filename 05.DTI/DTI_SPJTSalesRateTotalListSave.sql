CREATE PROC dbo.DTI_SPJTSalesRateTotalListSave
    @xmlDocument    NVARCHAR(MAX), 
    @xmlFlags       INT = 0, 
    @ServiceSeq     INT = 0, 
    @WorkingTag     NVARCHAR(10)= '', 
    @CompanySeq     INT = 1, 
    @LanguageSeq    INT = 1, 
    @UserSeq        INT = 0, 
    @PgmSeq         INT = 0 
AS 
    
    CREATE TABLE #DTI_TPJTSalesRateTotal (WorkingTag NCHAR(1) NULL)  
    EXEC dbo._SCAOpenXmlToTemp @xmlDocument, @xmlFlags, @CompanySeq, @ServiceSeq, 'DataBlock3', '#DTI_TPJTSalesRateTotal'     
    IF @@ERROR <> 0 RETURN  
--select * from #DTI_TPJTSalesRateTotal 
--return 

    DECLARE @ClosingNextCostYM NCHAR(6)
    SELECT @ClosingNextCostYM = ISNULL((
                                         SELECT CONVERT(NCHAR(6),DATEADD(MONTH, 1, MAX(A.CostYM) + '01'),112)
                                           FROM _TESMDCostKey AS A 
                                           JOIN _TESMCProfClosing AS B ON ( B.CompanySeq = @CompanySeq AND B.CostKeySeq = A.CostKeySeq ) 
                                          WHERE B.IsClosing = '1'
                                            AND A.CompanySeq = @CompanySeq 
                                       ),'19000101'
                                      )
    
    SELECT MAX(CASE WHEN A.TITLE_IDX1_SEQ = 100 THEN A.Results ELSE 0 END) AS Qty ,
           MAX(CASE WHEN A.TITLE_IDX1_SEQ = 200 THEN A.Results ELSE 0 END) AS SalesAmt, 
           MAX(CASE WHEN A.TITLE_IDX1_SEQ = 300 THEN A.Results ELSE 0 END) AS SalesCost, 
           ItemSeq, ItemKindSeq, TITLE_IDX0_SEQ, PJTSeq
      INTO #DTI_TPJTSalesRateTotal_SUB
      FROM #DTI_TPJTSalesRateTotal AS A 
     WHERE A.TITLE_IDX0_SEQ = @ClosingNextCostYM
     GROUP BY ItemSeq, ItemKindSeq, TITLE_IDX0_SEQ, PJTSeq
    
    IF EXISTS (SELECT 1 
                 FROM #DTI_TPJTSalesRateTotal_SUB AS A 
                 JOIN DTI_TPJTSalesRateTotal      AS B ON ( B.CompanySeq = @CompanySeq 
                                                        AND B.PJTSeq = A.PJTSeq 
                                                        AND B.ItemSeq = A.ItemSeq 
                                                        AND B.SMItemType = A.ItemKindSeq 
                                                        AND B.ResultYM = A.TITLE_IDX0_SEQ 
                                                          )
              )
    BEGIN 
        UPDATE A
           SET Qty = B.Qty, 
               SalesAmt = B.SalesAmt, 
               SalesCost = B.SalesCost
          FROM DTI_TPJTSalesRateTotal AS A 
          JOIN #DTI_TPJTSalesRateTotal_SUB AS B ON ( B.PJTSeq = A.PJTSeq AND B.ItemSeq = A.ItemSeq AND B.ItemKindSeq = A.SMItemType AND B.TITLE_IDX0_SEQ = A.ResultYM ) 
    END
    
    -- INSERT
    IF NOT EXISTS (SELECT 1 
                     FROM #DTI_TPJTSalesRateTotal_SUB AS A 
                     JOIN DTI_TPJTSalesRateTotal      AS B ON ( B.CompanySeq = @CompanySeq 
                                                            AND B.PJTSeq = A.PJTSeq 
                                                            AND B.ItemSeq = A.ItemSeq 
                                                            AND B.SMItemType = A.ItemKindSeq 
                                                            AND B.ResultYM = A.TITLE_IDX0_SEQ 
                                                              ) 
                  )
    BEGIN  
        INSERT INTO DTI_TPJTSalesRateTotal
        (
            CompanySeq, PJTSeq, ResultYM, SMItemType, ItemSeq, 
            Serl, Qty, SalesAmt, SalesCost, LastUserSeq, 
            LastDateTime
        ) 
        SELECT @CompanySeq, A.PJTSeq, A.TITLE_IDX0_SEQ, A.ItemKindSeq, A.ItemSeq, 
               1, A.Qty, A.SalesAmt, A.SalesCost, @Userseq, 
               GETDATE()
          FROM #DTI_TPJTSalesRateTotal_SUB AS A   

    IF @@ERROR <> 0 RETURN
    END   

    SELECT * FROM #DTI_TPJTSalesRateTotal 
    
    RETURN 
GO
